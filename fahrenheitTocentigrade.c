//PROGRAM TO CONVEST FAHRENHEIT TO CENTIGRADE DEGREES
#include<stdio.h>
int main(){
    float far,c;
    printf("enter the value of f="); // far denotes the fahrenheit
    scanf("%f",&far);
    c=(5*far-32)/9;
    printf(" tempereture from fahreheit to centigrade =%f",c);
    
    return 0;

}